    public void validasiInput(String nim, String nama, int presensi, int tugas, int uts, int uas) {
        if ((presensi < 0 || presensi > 100) || (tugas < 0 || tugas > 100) || (uts < 0 || uts > 100) || (uas < 0 || uas > 100)) {
            lblInputSalah.setText("Nilai harus diantara 0 dan 100!!");
            lblInputSalah.setVisible(true);
            txtNilaiAkhir.setText("");
            lblMatkul.setText("");
        } else if (nim == null || nim.equalsIgnoreCase("") || nama == null || nama.equalsIgnoreCase("")) {
            lblInputSalah.setText("Nama atau NIM tidak boleh kosong!!");
            lblInputSalah.setVisible(true);
            txtNilaiAkhir.setText("");
            lblMatkul.setText("");
        }
    }
