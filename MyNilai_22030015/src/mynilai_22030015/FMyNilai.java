    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // TODO add your handling code here:   
        try {
            String nim = txtNim.getText();
            String nama = txtNama.getText();
            int presensi = Integer.parseInt(txtJmlhPresensi.getText());
            int tgs = Integer.parseInt(txtTgs.getText());
            int uts = Integer.parseInt(txtUts.getText());
            int uas = Integer.parseInt(txtUas.getText());
            String matkul = cbxMatkul.getSelectedItem().toString();
            char grade;
        
            double nilaiAkhir = (0.1 * presensi) + (0.3 * tgs) + (0.3 * uts) + (0.3 * uas);
       
            if (nilaiAkhir >= 80) {
                grade = 'A';
            } else if (nilaiAkhir >= 65 && nilaiAkhir <= 79.9) {
                grade = 'B';
            } else if (nilaiAkhir >= 55 && nilaiAkhir <= 64.9) {
                grade = 'C';
            } else if (nilaiAkhir >= 40 && nilaiAkhir <= 54.9) {
                grade = 'D';
            } else {
                grade = 'E';
            }
        
            lblMatkul.setText(matkul);
            String resume = "\nNIM\t: " + nim + "\n\n" +
                            "Nama\t: " + nama + "\n\n" +
                            "Nilai Akhir\t: " + nilaiAkhir + "\n\n" +
                            "Grade\t: " + grade;
            
            txtNilaiAkhir.setText(resume);
            lblInputSalah.setVisible(false); 
            validasiInput(nim, nama, presensi, tgs, uts, uas);
        } catch (NumberFormatException e) {
            lblInputSalah.setText("Input tidak boleh kosong dan nilai harus berupa angka!!");
            lblInputSalah.setVisible(true);
        } 
    }//GEN-LAST:event_btnHitungActionPerformed

